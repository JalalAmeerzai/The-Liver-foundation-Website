                        <h3 style="color: #9b2061;">Fibro Scanning</h3>
                        <p>Fibro scanning of a liver is advanced test of imaging. It is done to check the level of softness of liver tissues. The stiff will be the liver, the more will be the extent of liver disease. 

The liver Foundation provides facility of fibro scanning free of cost. It is done using a device called the FibroScan, our doctor places a handheld probe against patient’s abdomen, and the probe sends a painless vibration through the body and into the liver. A transducer on the end of the probe measures how long it takes for the vibration to travel through the liver. The faster the vibration passes through, the stiffer the liver is.</p>
                    </div>
                </div>



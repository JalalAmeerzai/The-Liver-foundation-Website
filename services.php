                        <h3 style="color: #9b2061;">Ultrasound</h3>
                        <p>The liver Foundation provides the facility of ultrasound of the patients with hepatitis B and C to check whether the liver is inflamed or not. Ultrasound is a painless test in which high frequency sound waves are used to produce inner structural images of patient’s body. 

Our doctors places a handheld probe called a transducer against patient’s abdomen, and the transducer sends an image of the liver to a computer monitor, where it is analyzed for further treatment.</p>
                    </div>
                </div>



                        <h3 style="color: #9b2061;">Medication</h3>
                        <p>The Liver Foundation provides appropriate medicine of all kinds of chronic hepatitis. Patients are followed up after every two weeks which is continued for 3 months and in some cases for 6 months. After completion of medication , the patients are again called for the blood tests to ensure that they are free from hepatitis and are further advised by doctors about prevention and care.</p>

                        <h3 style="color: #9b2061;">Blood Screening</h3>
                        <p>Blood screening here in The Liver Foundation is done by the highly trained staff with professionalism and under hygienic environment. It involves testing a blood sample of patient to see whether it contains Hepatitis virus. Hepatitis blood tests look for antigens or antibodies specific to each type of hepatitis virus such as hepatitis A, B or C. 

The negative result of blood screening indicates that the patient is not diagnosed with hepatitis virus. If the result is positive then the type of Hepatitis is determined and the patient is treated further accordingly.</p>
